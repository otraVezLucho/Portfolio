/*
1. Acceder a elementos del DOM
Para manipular elementos, primero necesitas seleccionarlos. Aquí están los métodos más usados:

document.getElementById(id): Selecciona un elemento por su id.
document.querySelector(selector): Selecciona el primer elemento que coincida con un selector CSS (como .clase, #id, div).
document.querySelectorAll(selector): Devuelve una lista de todos los elementos que coincidan con el selector.
document.getElementsByClassName(class): Selecciona elementos por su clase.
document.getElementsByTagName(tag): Selecciona elementos por su etiqueta (como p, div).

*/